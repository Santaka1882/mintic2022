for x in range(5):
  print(x)  #numeros del 0 al 5

for x in range(10, 21):
  print(x)  #numeros del 10 al 20

for x in range(0, 12, 2):
  print(x) #Numeros del 0 al 10 pero solo multiplos de 2

for x in range(12, 0, -2):
  print(x) #Numeros del 12 al 0 pero solo multiplos de 2 y en orden decendente